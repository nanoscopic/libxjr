void escape_inplace( char *str, int len, int *outlen ) {
	int offset=0;
	int i=0;
	for(i=0;i<len;i++) {
		if( str[i] == '\\' ) break;
	}
	if( i==len ) {
		*outlen = len;
		return;
	}
	for( ;i<len;i++ ) {
		char let = str[i];
		if( let == '\\' ) {
			str[i-offset] = str[i+1];
			offset++;
			i++;
			continue;
		}
		str[i-offset] = let;
	}
	*outlen = len - offset;
	str[ *outlen ] = 0x00;
}


